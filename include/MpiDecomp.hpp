#pragma once
#include <mpi.h>
#include <vector>

// ============================================================================
// MpiDecomp — MPI domain decomposition (декомпозиция области).
//
// Идея из AMReX/OpenFOAM: глобальная сетка NX × NY разбивается на полосы
// по оси Y. Каждый MPI-процесс (ранк) владеет своей полосой:
//
//   rank 0: строки j = [0,           local_ny_0)
//   rank 1: строки j = [local_ny_0,  local_ny_0 + local_ny_1)
//   ...
//
// Для корректного расчёта потоков на границах между доменами нужны
// ghost-ячейки (halo) — копии граничных строк соседних ранков.
//
//   +-------------------+
//   | ghost (от rank+1) |  ← 1 строка, копия нижней строки верхнего соседа
//   |===================|
//   |                   |
//   | реальные ячейки   |  ← local_ny строк, «свои» данные
//   |                   |
//   |===================|
//   | ghost (от rank-1) |  ← 1 строка, копия верхней строки нижнего соседа
//   +-------------------+
//
// Halo exchange: MPI_Sendrecv — отправляем свою граничную строку соседу
// и одновременно получаем его граничную строку в свой ghost.
//
// Для GPU: сначала cudaMemcpy D→H halo-строк, потом MPI, потом H→D.
// (Можно ускорить через CUDA-aware MPI, если кластер поддерживает.)
// ============================================================================
struct MpiDecomp {
    int rank = 0;            // номер текущего процесса
    int size = 1;            // общее число процессов
    int global_nx = 0;       // глобальный размер по X
    int global_ny = 0;       // глобальный размер по Y
    int local_ny  = 0;       // число строк на этом ранке (без ghost)
    int j_start   = 0;       // глобальный j первой строки этого ранка

    // Соседи (для периодических ГУ: rank 0 граничит с rank size-1)
    int rank_below = -1;     // сосед снизу (j меньше)
    int rank_above = -1;     // сосед сверху (j больше)

    MPI_Comm comm = MPI_COMM_WORLD;

    // Инициализация: вызывается после MPI_Init.
    // Делит NY строк между size ранками максимально равномерно.
    void init(int nx, int ny);

    // Обмен ghost-строками температуры T между соседними ранками.
    // T — массив размера nx * (local_ny + 2): ghost_bottom, реальные, ghost_top.
    // nx — ширина строки.
    //
    // Схема обмена (MPI_Sendrecv):
    //   отправляем нижнюю реальную строку → ghost_top соседа снизу
    //   получаем от соседа снизу его верхнюю строку → наш ghost_bottom
    //   аналогично сверху
    void exchange_halos(float* T, int nx, int local_ny_with_ghosts, int nvar = 1, int ncells_total = 0);

    void finalize();
};
